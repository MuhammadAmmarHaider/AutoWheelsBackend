// ─── MAIN SEED FUNCTION ───────────────────────────────────────
async function main() {
  console.log("🚗 Starting Pakistan Cars Seed...\n");

  // 1. Seed Brands
  console.log("📦 Seeding Brands...");
  const brandMap: Record<string, string> = {};
  for (const brand of brands) {
    const created = await prisma.brand.upsert({
      where: { name: brand.name },
      update: { country: brand.country },
      create: {
        name: brand.name,
        country: brand.country,
      },
    });
    brandMap[brand.name] = created.id;
    console.log(`  ✅ Brand: ${brand.name} (${brand.country})`);
  }

  // 2. Seed Models
  console.log("\n📦 Seeding Models...");
  const modelMap: Record<string, string> = {};
  for (const model of models) {
    const brandId = brandMap[model.brandName];
    if (!brandId) {
      console.warn(`  ⚠️  Brand not found for model: ${model.name}`);
      continue;
    }
    const created = await prisma.carModel.upsert({
      where: { name_brandId: { name: model.name, brandId } },
      update: {},
      create: { name: model.name, brandId },
    });
    modelMap[`${model.brandName}::${model.name}`] = created.id;
    console.log(`  ✅ Model: ${model.brandName} ${model.name}`);
  }

  // 3. Seed Cars + all sub-tables
  console.log("\n🚘 Seeding Cars...");
  let count = 0;

  for (const car of cars) {
    const brandId = brandMap[car.brandName];
    const modelId = modelMap[`${car.brandName}::${car.modelName}`];

    if (!brandId || !modelId) {
      console.warn(`  ⚠️  Skipping ${car.name} - brand/model not found`);
      continue;
    }

    // Upsert the main NewCar record
    const newCar = await prisma.newCar.upsert({
      where: { name_year_brandId: { name: car.name, year: car.year, brandId } },
      update: {
        priceMin: car.priceMin,
        priceMax: car.priceMax,
      },
      create: {
        brandId,
        modelId,
        name: car.name,
        year: car.year,
        priceMin: car.priceMin,
        priceMax: car.priceMax,
        bodyType: car.bodyType,
        engine: car.engine,
        horsepower: car.horsepower,
        torque: car.torque,
        fuelType: car.fuelType,
        transmission: car.transmission,
        mileage: car.mileage,
        seating: car.seating,
        topSpeed: car.topSpeed,
        safetyRating: car.safetyRating ? parseFloat(car.safetyRating) : null,
        airbags: car.airbags,
        infotainment: car.infotainment,
        features: car.features,
        description: car.description,
        launchDate: car.launchDate,
      },
    });

    const carId = newCar.id;

    // --- Dimensions ---
    if (car.dimensions) {
      const d = car.dimensions;
      await prisma.carDimensions.upsert({
        where: { newCarId: carId },
        update: {
          length: d.length,
          width: d.width,
          height: d.height,
          wheelbase: d.wheelbase,
          groundClearance: d.groundClearance,
          bootSpace: d.bootSpace,
          doors: d.doors,
          seating: car.seating,
        },
        create: {
          newCarId: carId,
          length: d.length,
          width: d.width,
          height: d.height,
          wheelbase: d.wheelbase,
          groundClearance: d.groundClearance,
          bootSpace: d.bootSpace,
          doors: d.doors,
          seating: car.seating,
        },
      });
    }

    // --- Engine Details ---
    if (car.engineDetails) {
      const e = car.engineDetails;
      // Map drivetrain string to enum
      const drivetrainMap: Record<string, string> = {
        FWD: "FWD",
        RWD: "RWD",
        AWD: "AWD",
        "4WD": "FOURWD",
        "FWD/AWD": "AWD",
      };
      const drivetrain = drivetrainMap[e.drivetrain] ?? "FWD";

      await prisma.carEngineDetails.upsert({
        where: { newCarId: carId },
        update: {
          type: e.type,
          turbocharged: e.turbocharged,
          displacement: e.displacement,
          cylinders: e.cylinders,
          drivetrain: drivetrain as any,
          fuelSystem: e.fuelSystem,
          valveMechanism: e.valveMechanism,
          maxSpeed: e.maxSpeed,
        },
        create: {
          newCarId: carId,
          type: e.type,
          turbocharged: e.turbocharged,
          displacement: e.displacement,
          cylinders: e.cylinders,
          drivetrain: drivetrain as any,
          fuelSystem: e.fuelSystem,
          valveMechanism: e.valveMechanism,
          maxSpeed: e.maxSpeed,
        },
      });
    }

    // --- Transmission Details ---
    if (car.transmissionDetails) {
      const t = car.transmissionDetails;
      await prisma.carTransmissionDetails.upsert({
        where: { newCarId: carId },
        update: { type: t.type, gearbox: t.gearbox },
        create: { newCarId: carId, type: t.type, gearbox: t.gearbox },
      });
    }

    // --- Steering ---
    if (car.steering) {
      const s = car.steering;
      await prisma.carSteering.upsert({
        where: { newCarId: carId },
        update: {
          type: s.type,
          turningRadius: s.turningRadius,
          powerAssisted: s.powerAssisted,
        },
        create: {
          newCarId: carId,
          type: s.type,
          turningRadius: s.turningRadius,
          powerAssisted: s.powerAssisted,
        },
      });
    }

    // --- Suspension ---
    if (car.suspension) {
      const s = car.suspension;
      await prisma.carSuspension.upsert({
        where: { newCarId: carId },
        update: {
          front: s.front,
          frontBrakes: s.frontBrakes,
          rear: s.rear,
          rearBrakes: s.rearBrakes,
        },
        create: {
          newCarId: carId,
          front: s.front,
          frontBrakes: s.frontBrakes,
          rear: s.rear,
          rearBrakes: s.rearBrakes,
        },
      });
    }

    // --- Wheels ---
    if (car.wheels) {
      const w = car.wheels;
      await prisma.carWheels.upsert({
        where: { newCarId: carId },
        update: {
          wheelType: w.wheelType,
          tyreSize: w.tyreSize,
          wheelSize: w.wheelSize,
          pcd: w.pcd,
          spareTyreSize: w.spareTyreSize,
        },
        create: {
          newCarId: carId,
          wheelType: w.wheelType,
          tyreSize: w.tyreSize,
          wheelSize: w.wheelSize,
          pcd: w.pcd,
          spareTyreSize: w.spareTyreSize,
        },
      });
    }

    // --- Fuel Economy (skip for pure electrics) ---
    if (car.fuelEconomy && car.fuelType !== "ELECTRIC") {
      const f = car.fuelEconomy;
      await prisma.carFuelEconomy.upsert({
        where: { newCarId: carId },
        update: {
          cityMileage: f.cityMileage,
          highwayMileage: f.highwayMileage,
          tankCapacity: f.tankCapacity,
        },
        create: {
          newCarId: carId,
          cityMileage: f.cityMileage,
          highwayMileage: f.highwayMileage,
          tankCapacity: f.tankCapacity,
        },
      });
    }

    count++;
    console.log(`  ✅ [${count}/${cars.length}] ${car.name} (${car.year})`);
  }

  console.log(`\n✨ Seed complete! Seeded:`);
  console.log(`   - ${brands.length} brands`);
  console.log(`   - ${models.length} models`);
  console.log(`   - ${count} cars (with all sub-tables)`);
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });